Numero = int(input("Ingrese un numero: "))

if (Numero > 0 and Numero < 10):
    print ("El numero pertenece al rango de 0 a 10")
elif (Numero > 10 and Numero < 20):
    print ("El numero pertenece al rango de 10 a 20")
elif (Numero > 20 and Numero < 30):
    print ("El numero pertenece al rango de 20 a 30")
elif (Numero > 30 and Numero < 40):
    print ("El numero pertenece al rango de 30 a 40")
elif (Numero > 40 and Numero < 50):
    print ("El numero pertenece al rango de 40 a 50")
else:
    print("El numero NO pertenece a ningun rango")
